package operators;

public class BitWiseOperators {
    public static void main(String[] args) {
        //Bitwise AND (||) 1 1=1
        int a =10;
        int b=8;
        int c=10&8;
        System.out.println(c);

        //Bitwise Inlusive OR

        int d =10;
        int e=8;
        int f=10|8;
        System.out.println(f);

        //Bitwise Exclusive OR

        int g=10;
        int h=8;
        int i=10^8;
        System.out.println(i);
    }
}
